/*
 An array is a list of items.
 All arrays in JavaScript are mutable
*/


var assert = require('assert');

var fruits = ["Apple", "Banana", "Mango"];

assert.equal(fruits[0], "Apple", "Values must match");
assert.equal(fruits[1], "Banana", "Values must match");
assert.equal(fruits[2], "Mango", "Values must match");
